       <strong> What is sourdough?</strong>
    Sourdough is naturally leavened bread, which means it doesn’t use 
    commercial yeast to rise. Instead, it uses a ‘starter’ – a fermented 
    flour and water mixture that contains wild yeast and good bacteria 
    – to rise. This also produces the tangy flavour and slightly chewy 
    texture you’ll find in sourdough. Wild yeast has more flavour than 
    commercial yeast, and is natural in the sense that it doesn’t contain
    any additives.
    <strong>How to make sourdough</strong>
    Basic sourdough is made from nothing more than flour, water and 
    salt – here’s what you need to know about each.
    Flour – sourdough can be made with many types of flour, but for
    our basic recipe, we’ll be using strong white bread flour. It’s 
    the most readily available, and means you’ll only need to buy 
    one flour to start. But, we know ingredients are hard to come 
    by at the moment, so you can also use strong wholemeal 
    bread flour.
    Water – good old tap water is absolutely fine, and it’s the only 
    thing we’ve ever used to make our sourdough. Don’t waste 
    money on minerals.
    Salt – fine, pure sea salt is the best, but if you don’t have it, you 
    can use any type of cooking salt you have, except rock salt – 
    that will need to be crushed before using.
    You can use different flours in our basic recipe without changing 
    the amount of water you use or the method in any way, other than 
    adding less strong white bread flour. Here are the ideal 
    proportions:
    For wholemeal, granary, seeded or malted flour use 350g 
    strong white bread flour and 150g of the other flour so you 
    still have 500g total.
    For rye, spelt, emmer, buckwheat or Khorasan flour use 
    400g strong white bread flour and 100g of the other flour so 
    you still have 500g total.
    You can experiment with different proportions, but bear in mind 
    that other flours will be ‘thirstier’, so you’ll then need to adjust the 
    amount of water accordingly. But once you know what texture 
    you’re looking for in the dough, this will be easy to do.
    The basic dough is a blank canvas to which you can add anything 
    you like. Flavouring ingredients should be scattered over the 
    dough, or worked into it during the final two folds. We’ve added 
    seeds, nuts, grains, olives, herbs, chillies, chunks of cheese and 
    dried fruit to our sourdough.
